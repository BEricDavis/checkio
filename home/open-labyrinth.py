import collections

s = (1, 1)


def find_children(p, maze_map, bfs):
    """Find adjacent nodes.  If a node is a 0 (a valid path) and has not been seen
    add it to the list of children."""
    child_list = []
    moves = {(1, 0): "S", (-1, 0): "N", (0, -1): "W", (0, 1): "E"}

    for move in moves:
        check_x = p[0] + move[0]
        check_y = p[1] + move[1]
        node = (check_x, check_y)
        if (check_x, check_y) not in bfs and maze_map[check_x][check_y] == 0:
            child_list.append((check_x, check_y))
            bfs[node]['parent'] = p
            bfs[node]['move'] = moves[move]
    return child_list


def checkio(maze_map):
    bfs = collections.defaultdict(lambda: collections.defaultdict(dict))
    bfs[s]['parent'] = None
    bfs[s]['move'] = None
    i = 1
    frontier = [s]  # level i - 1

    while frontier:
        next_batch = []  # level i
        for u in frontier:
            for v in find_children(u, maze_map, bfs):
                next_batch.append(v)
        frontier = next_batch
        i += 1

    node = (10,10)
    path = []

    while node != (1, 1):
        parent = bfs[node]['parent']
        path.append(bfs[node]['move'])
        node = parent

    path.reverse()

    final_path = ''
    for d in path:
        final_path += d
    return final_path


